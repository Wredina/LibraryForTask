from random import randint
size = randint(5, 10)  # рандомный размер массива
numX = randint(0, 10)  # рандомное искомое число
print(numX)

# # # ВАРИАНТ 1 - если числа в массиве идут от меньшего к большему ###########
array = [i for i in range(1, size)]
print(array)

# МЕТОД РЕШЕНИЯ ВАРИАНТА 1 - 1
# поиск через строковый метод

# воспользуемся строковым методом поиска, который выводит True или False
# find_x =

if numX in array:  # если True, т.е. число в массиве есть
    print(numX)  # выводим загаданное число
else:  # если False
    # проверяем, больше ли загаданное число последнего эл-та.
    if numX > array[-1]:
        print(array[-1])  # если да, выводим последнее число
    elif numX < array[0]:  # проверяем, меньше ли данное число первого эл-та
        print(array[0])  # если да, выводим первое число


# # # ## МЕТОД РЕШЕНИЯ ВАРИАНТА 1 - 2
# # через цикл for и флажочек

flag = False  # устанавливаем флаг, он загориться если найдём элемент в массиве
for elem in array:  # перебираем все элементы в поиске загаданного числа
    if elem == numX:  # если находим нужное число
        flag = True  # зажигаем лампочку
        print(elem)  # и печатаем это число
        break  # останавливаем принудительно цикл, т.к. смысла продолжать его нет

if flag == False:  # если загаданное число не нашли в массиве
    if numX > array[-1]:  # просто проверяем, больше ли оно последнего эл-та массива
        # если да, печатаем его. Если нет, проваливаемся дальше
        print(array[-1])
    elif numX < array[0]:  # проверяем, загаданное число меньше первого эл-та
        print(array[0])  # если да, печатаем его


